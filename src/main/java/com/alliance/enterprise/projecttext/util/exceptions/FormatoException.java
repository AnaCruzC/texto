package com.alliance.enterprise.projecttext.util.exceptions;

public class FormatoException extends Exception {
	
	/**
	 * serialVersionUID
	 */
	private static final long serialVersionUID = 1L;

	public FormatoException() {
		super();
	}

	public FormatoException(String message) {
		super(message);
	}

	public FormatoException(String message, Throwable cause) {
		super(message, cause);
	}

	public FormatoException(Throwable cause) {
		super(cause);
	}

}
